#I დავალება: მომხმარებელს შემოატანინეთ ორი რიცხვი, შეამოწმეთ თუ პირველი რიცხვი მეტია მეორე რიცხვზე დაპრინტე რომ ‘first is
#more than second’, ასევე შეამოწმე თუ პირველი რიცხვი ნაკლებია მეორე რიცხვზე დაპრინტე რომ ‘first is less than second’ და სხვა
#დანარჩენ შემთხვევაში დაპტინტე რომ ‘first number equal to second number’.
first = int(input("შეიყვანე პირველი რიცხვი: "))
second = int(input("შეიყვანე მეორე რიცხვი: "))

if first > second:
    print("პირველი მეტია მეორეზე")
elif first < second:
    print("პირველი ნაკლებია მეორეზე")
else:
    print("პირველი რიცხვი უდრის მეორე რიცხვს")


