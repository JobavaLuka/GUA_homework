#III დავალება: შექმენი ორი ცვლადი სადაც შეინახავთ ინტეჯერ ტიპოს მონაცემებს, თქვენი დავალებაა შეამოწმოთ, თუ პირველი რიცხვი მეტია
#0ზე და და მეორე რიცხვიც მეტია 0 ზე დაუპრინტე რომ ‘ორივე რიცხვი დადებითია, ასევე შეამოწმე თუ პირველი რიცხვი ნაკლებია 0ზე და
#მეორე რიცხვიც ნაკლებია 0 ზე დაპურინტე რომ  ‘ორივე რიცხვი არის უარყოფით’, სხვა დანარჩენ შემთხვევაში დაუპრინტე რომ ‘ეს რა
#ჯანდაბაა’.
