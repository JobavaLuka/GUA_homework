num2 = int(input("შეიყვანე მეორე რიცხვი: "))

if num1 > 0 and num2 > 0:
    print("ორივე რიცხვი დადებითია")
elif num1 < 0 and num2 < 0:
    print("ორივე რიცხვი დადებითია")
else:
    print("ეს რა ჯანდაბაა")


